#include "libft.h"

int		ft_fac(int nb)
{
	if (nb < 0)
		return (0);
	if (nb >= 0 && nb < 13)
	{
		if (nb == 0 || nb == 1)
			return (1);
		if (nb > 1 && nb < 13)
			return (nb * ft_fac(nb - 1));
	}
	return (0);
}
