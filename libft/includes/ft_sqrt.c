#include "libft.h"

int				ft_sqrt(double nb)
{
	int		x;

	if (nb < 0)
		return (-1);
	x = 1;
	if (nb == 0)
		return (0);
	while (x * x != nb)
		x++;
	if (x * x == nb)
		return (x);
	return (0);
}
